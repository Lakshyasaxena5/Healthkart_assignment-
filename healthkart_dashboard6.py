st.markdown("### ü•á Revenue Share by Product")
product_rev = filtered_tracking.groupby('product')['revenue'].sum()
if not product_rev.empty:
    fig, ax = plt.subplots(facecolor='#f0f2f6')
    ax.set_facecolor('#f0f2f6')
